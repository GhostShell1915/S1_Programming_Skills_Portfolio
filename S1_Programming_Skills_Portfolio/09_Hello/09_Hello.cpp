#include <iostream>
using namespace std;

// Define the hello function with void return type since it doesn't return a value
void hello() {
    // Use cout to print "Hello" to the console, followed by endl for a newline
    cout << "Hello" << endl;
}

int main() {
    // Call the hello function to execute its code
    hello();
    
    // Indicate successful program completion
    return 0;
}