#include <iostream>

int main() {
    // Declare and initialize the first integer variable
    int num1 = 8;
    
    // Declare and initialize the second integer variable
    int num2 = 10;
    
    // Declare a third integer variable that adds the previous two
    int sum = num1 + num2;
    
    // Print the value of the sum to the console
    std::cout << "The sum is: " << sum << std::endl;
    
    return 0;
}